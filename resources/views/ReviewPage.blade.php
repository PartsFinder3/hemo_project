<script>
document.addEventListener('DOMContentLoaded', function(){
    const slider = document.querySelector('.abdul-review-slider');
    const leftBtn = document.querySelector('.abdul-slide-btn.abdul-left');
    const rightBtn = document.querySelector('.abdul-slide-btn.abdul-right');
    const dots = document.querySelectorAll('.abdul-dot');
    const cards = document.querySelectorAll('.abdul-review-card');

    let currentIndex = 0;

    function getCardWidth(){
        const gap = parseInt(getComputedStyle(slider).gap)||0;
        return cards[0].offsetWidth + gap;
    }

    function getMaxIndex(){
        const visibleCards = Math.floor(slider.parentElement.clientWidth / getCardWidth());
        return Math.max(cards.length - visibleCards,0);
    }

    function updateSliderPosition(){
        slider.style.transform = `translateX(-${currentIndex*getCardWidth()}px)`;
        dots.forEach((dot,i)=>dot.classList.toggle('abdul-active',i===currentIndex));
    }

    rightBtn.addEventListener('click',()=>{
        currentIndex++;
        if(currentIndex>getMaxIndex()) currentIndex=getMaxIndex();
        updateSliderPosition();
    });

    leftBtn.addEventListener('click',()=>{
        currentIndex--;
        if(currentIndex<0) currentIndex=0;
        updateSliderPosition();
    });

    dots.forEach(dot=>{
        dot.addEventListener('click',()=>{
            currentIndex=parseInt(dot.getAttribute('data-index'));
            if(currentIndex>getMaxIndex()) currentIndex=getMaxIndex();
            updateSliderPosition();
        });
    });

    let autoSlide=setInterval(()=>{
        if(currentIndex<getMaxIndex()) currentIndex++;
        else currentIndex=0;
        updateSliderPosition();
    },5000);

    slider.parentElement.addEventListener('mouseenter',()=>clearInterval(autoSlide));
    slider.parentElement.addEventListener('mouseleave',()=>{
        autoSlide=setInterval(()=>{
            if(currentIndex<getMaxIndex()) currentIndex++;
            else currentIndex=0;
            updateSliderPosition();
        },5000);
    });

    window.addEventListener('resize',()=>{
        if(currentIndex>getMaxIndex()) currentIndex=getMaxIndex();
        updateSliderPosition();
    });

    updateSliderPosition();
});
</script>
